#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return self.likersArray.count;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"likersId"];
    
    cell.textLabel.text = ((SBInstagramUserEntity *)self.likersArray[indexPath.row]).fullName;
    [cell.imageView setImageWithURL:[NSURL URLWithString:((SBInstagramUserEntity *)self.likersArray[indexPath.row]).profilePictureUrl] placeholderImage:[UIImage imageNamed:@"SBInstagramLoading.png"]];
    
    return cell;
}


